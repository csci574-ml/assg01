import pytest
import random
import numpy as np
from assg_tasks import one_hot


def test_example_case():
    category = np.array(['Setosa', 'Versicolor', 'Setosa', 'Virginica', 'Virginica'])
    one_hot_array = one_hot(category)
    expected_one_hot_array = np.array(
        [[1., 0., 0.],
            [0., 1., 0.],
            [1., 0., 0.],
            [0., 0., 1.],
            [0., 0., 1.]])
    
    assert one_hot_array.shape == (5,3)
    assert np.allclose(one_hot_array, expected_one_hot_array)


